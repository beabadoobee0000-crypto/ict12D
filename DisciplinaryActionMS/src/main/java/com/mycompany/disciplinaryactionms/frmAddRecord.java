    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
    String studentId = txtStudentId.getText();
    String studentName = txtStudentName.getText();
    String gradeSection = cmbGrade.getSelectedItem().toString();
    String offense = cmbOffenseC.getSelectedItem().toString();
    String suspension = spnSuspension.getValue().toString();
    String offenseC = cmbOffenseC.getSelectedItem().toString();
    java.util.Date date = (java.util.Date) spnDate.getValue();
    java.sql.Timestamp sqlDate = new java.sql.Timestamp(date.getTime());
    String Image = imagePath;
    
   
    if(studentId.isEmpty() || studentName.isEmpty() || gradeSection.isEmpty() || offense.isEmpty() || suspension.isEmpty()|| offenseC.isEmpty()|| imagePath == null) {
    javax.swing.JOptionPane.showMessageDialog(null, "Please fill all fields!");
    return;
    }
    try {
    System.out.println("IMAGE PATH SAVED: " + imagePath);
    ConnectXamppSQL.Insert("addrecord")
        .set("Student_Id", studentId)
        .set("Student_Name", studentName)
        .set("Grade_Section", gradeSection)
        .set("Offense_Type", offense)
        .set("Days_of_Suspension", suspension)
        .set("Offense_Classification", offenseC)
        .set("Date_of_Incident", sqlDate)
        .set("image_path", imagePath)
        .execute();
         

    javax.swing.JOptionPane.showMessageDialog(null, "Record Saved!");
    new frmDashboardAdmin().setVisible(true);
    this.dispose();
    }
    catch (Exception e) {
    e.printStackTrace();
    JOptionPane.showMessageDialog(null, e.getMessage());
    }

    }//GEN-LAST:event_btnSaveActionPerformed

