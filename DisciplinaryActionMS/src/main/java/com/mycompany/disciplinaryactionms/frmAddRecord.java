    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
    JFileChooser file = new JFileChooser();
    file.setCurrentDirectory(new File(System.getProperty("user.home")));
    FileNameExtensionFilter filter = new FileNameExtensionFilter("*.image","jpg","gif","png","webp");
    file.addChoosableFileFilter(filter);
    int output = file.showOpenDialog(file);
    if(output==JFileChooser.APPROVE_OPTION){
    File  selectFile = file.getSelectedFile();
    String fileName = System.currentTimeMillis() + "_" + selectFile.getName();
    String newPath = "images/" + fileName;

    File dest = new File(newPath);


    try {
    java.nio.file.Files.copy(
    selectFile.toPath(),
    dest.toPath(),
    java.nio.file.StandardCopyOption.REPLACE_EXISTING);
    } 
    catch (Exception e) {
    e.printStackTrace();
    }
    
    imagePath = newPath;
    jLabel12.setIcon(imageAdjust(newPath, null));
    }
    }//GEN-LAST:event_jButton5ActionPerformed

